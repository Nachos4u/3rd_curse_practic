FONT_PATH = r"C:\Windows\Fonts\arial.ttf"
FONT_BOLD = r"C:\Windows\Fonts\arialbd.ttf"


def font(size, bold=False):
    return ImageFont.truetype(FONT_BOLD if bold else FONT_PATH, size)


def _wrap(d, s, f, max_w):
    words = s.split()
    lines, cur = [], ""
    for w in words:
        t = (cur + " " + w).strip()
        if d.textlength(t, font=f) <= max_w:
            cur = t
        else:
            if cur:
                lines.append(cur)
            cur = w
    if cur:
        lines.append(cur)
    return lines


def text_center(d, box, s, f, fill="black"):
    x0, y0, x1, y1 = box
    lines = _wrap(d, s, f, (x1 - x0) - 10)
    asc, desc = f.getmetrics()
    lh = asc + desc
    total = lh * len(lines)
    ty = y0 + ((y1 - y0) - total) / 2
    for ln in lines:
        tw = d.textlength(ln, font=f)
        d.text((x0 + ((x1 - x0) - tw) / 2, ty), ln, font=f, fill=fill)
        ty += lh


def arrow(d, p0, p1, color="#333", width=2):
    d.line([p0, p1], fill=color, width=width)
    ang = math.atan2(p1[1] - p0[1], p1[0] - p0[0])
    L = 11
    for da in (math.radians(24), -math.radians(24)):
        d.line([p1, (p1[0] - L * math.cos(ang + da), p1[1] - L * math.sin(ang + da))],
               fill=color, width=width)


def actor(d, x, y, name, f, color="black"):
    d.ellipse((x - 13, y - 42, x + 13, y - 16), outline=color, width=3)
    d.line([(x, y - 16), (x, y + 16)], fill=color, width=3)
    d.line([(x - 20, y - 4), (x + 20, y - 4)], fill=color, width=3)
    d.line([(x, y + 16), (x - 16, y + 44)], fill=color, width=3)
    d.line([(x, y + 16), (x + 16, y + 44)], fill=color, width=3)
    text_center(d, (x - 70, y + 46, x + 70, y + 70), name, f, fill=color)


